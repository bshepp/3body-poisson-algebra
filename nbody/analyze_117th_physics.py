    # This is the FULL level-3 generator from the bracket
    # {{{H12,H13},H12},{H12,H13}} in the quantum algebra.
    G_full = b3  # already contains hbar dependence

    print(f"\n{'='*70}")
    print(f"COMMUTATOR WITH TOTAL HAMILTONIAN")
    print(f"{'='*70}")

    H_total = H12 + H13 + H23

    # Check: does the quantum correction alone commute with H?
    print(f"\n  Computing [g_quantum, H_total]...")
    comm_gq_H = alg.commutator(g_quantum, H_total)
    comm_gq_H = cancel(comm_gq_H)
    if comm_gq_H == 0:
        print(f"  [g_quantum, H_total] = 0  -- CONSERVED!")
    else:
        n = len(Add.make_args(expand(comm_gq_H)))
        print(f"  [g_quantum, H_total] = {n} terms (NOT conserved individually)")

    # Check with individual pair Hamiltonians
    print(f"\n  Individual pair commutators:")
    for label, H_i in [("H12", H12), ("H13", H13), ("H23", H23)]:
        comm = cancel(alg.commutator(g_quantum, H_i))
        if comm == 0:
            print(f"    [g_quantum, {label}] = 0")
        else:
            n = len(Add.make_args(expand(comm)))
            print(f"    [g_quantum, {label}] = {n} terms")

    # Now check the FULL generator (classical + quantum)
    print(f"\n  Computing [G_full, H_total]...")
    comm_full = alg.commutator(G_full, H_total)
    comm_full = cancel(comm_full)
    if comm_full == 0:
        print(f"  [G_full, H_total] = 0  -- THE FULL GENERATOR IS CONSERVED!")
    else:
        n = len(Add.make_args(expand(comm_full)))
        orders_comm = separate_hbar_orders(comm_full)
        print(f"  [G_full, H_total] = {n} terms")
        print(f"  hbar orders present: {sorted(orders_comm.keys())}")
        for k, v in sorted(orders_comm.items()):
            nv = len(Add.make_args(expand(v)))
            print(f"    hbar^{k}: {nv} terms")

    # Key question: is G_full in the center of the algebra?
    # The center elements commute with ALL generators, not just H.
    # But we know the level-2 center has dimension 11 (for 1/r).
    # A level-3 element might not be in the center.

