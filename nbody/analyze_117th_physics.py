    b2 = cancel(alg.commutator(b1, H12))
    b3 = cancel(alg.commutator(b2, b1))
    orders = separate_hbar_orders(b3)
    g_classical = expand(orders.get(0, Integer(0)))
    g_quantum = expand(orders.get(2, Integer(0)))
    print(f"  Classical part: {len(Add.make_args(g_classical))} terms")
    print(f"  Quantum correction: {len(Add.make_args(g_quantum))} terms")

