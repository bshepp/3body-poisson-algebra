def plot_growth(all_results, save_path="algebra_growth.png"):
    """Create a 4-panel growth-characterisation figure."""
    if not HAS_MPL:
        print("\n  matplotlib not available — skipping plot.")
        return

    fig, axes = plt.subplots(2, 2, figsize=(13, 10))
    fig.suptitle("Three-Body Lie Algebra Growth Characterisation",
                 fontsize=14, fontweight="bold", y=0.98)
    colors = plt.cm.tab10.colors

    # --- panel 1: dimension (linear) ----------------------------------------
    ax = axes[0, 0]
    for idx, (label, (results, _)) in enumerate(all_results.items()):
        lvls = [r[0] for r in results]
        dims = [r[1] for r in results]
        ax.plot(lvls, dims, "o-", color=colors[idx], label=label,
                markersize=8, linewidth=2)
    ax.set_xlabel("Bracket level L")
    ax.set_ylabel("Algebra dimension d(L)")
    ax.set_title("Cumulative dimension")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    # --- panel 2: dimension (log) -------------------------------------------
    ax = axes[0, 1]
    for idx, (label, (results, _)) in enumerate(all_results.items()):
        lvls = [r[0] for r in results]
        dims = [r[1] for r in results]
        ax.semilogy(lvls, dims, "o-", color=colors[idx], label=label,
                    markersize=8, linewidth=2)
        a = analyze_growth(results)
        if "exp_base" in a and len(lvls) >= 3:
            fit_x = np.linspace(lvls[0], lvls[-1], 50)
            fit_y = a["exp_coeff"] * a["exp_base"] ** fit_x
            ax.semilogy(fit_x, fit_y, "--", color=colors[idx], alpha=0.4)
    ax.set_xlabel("Bracket level L")
    ax.set_ylabel("d(L)  [log scale]")
    ax.set_title("Growth rate (log scale)")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    # --- panel 3: new generators per level ----------------------------------
    ax = axes[1, 0]
    n_cfg = len(all_results)
    bw = 0.8 / max(n_cfg, 1)
    for idx, (label, (results, _)) in enumerate(all_results.items()):
        lvls = [r[0] for r in results]
        ncs  = [r[2] for r in results]
        offset = (idx - n_cfg / 2 + 0.5) * bw
        ax.bar([l + offset for l in lvls], ncs,
               width=bw, color=colors[idx], label=label, alpha=0.8)
    ax.set_xlabel("Bracket level L")
    ax.set_ylabel("New independent generators")
    ax.set_title("New generators per level")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3, axis="y")

    # --- panel 4: growth ratio ----------------------------------------------
    ax = axes[1, 1]
    for idx, (label, (results, _)) in enumerate(all_results.items()):
        a = analyze_growth(results)
        if a["ratios"]:
            rx = list(range(1, len(a["ratios"]) + 1))
