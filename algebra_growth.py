#!/usr/bin/env python3
"""
Three-Body Lie Algebra Growth Characterization
================================================

Numerically determines how the Lie algebra generated by the three
pairwise Hamiltonians of a gravitational three-body system grows
under iterated Poisson brackets.

Key question:  Is the growth polynomial or exponential?
  - Polynomial  ->  residual algebraic structure constrains the chaos
  - Exponential ->  full chaos, complexity multiplies at each level
  - Mass-dependent ->  growth tied to physical configuration

The approach is fully numerical:
  1. Levels 0-1 use analytical formulas (exact).
  2. Levels 2+ use finite differences with adaptive step sizes.
  3. Linear independence is detected via QR projection / SVD at
     many random phase-space sample points.

Usage:
    python algebra_growth.py
    python algebra_growth.py --max-level 5 --samples 300
    python algebra_growth.py --help
"""

