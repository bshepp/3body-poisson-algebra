def run_full_sweep(max_level=3, n_samples=500):
    """Run all sweep types and print summary table."""
    all_results = []

    print("\n" + "=" * 70)
    print("FULL NEURAL NETWORK ALGEBRA SWEEP")
    print("=" * 70)

    all_results.extend(run_depth_sweep(max_level, n_samples))
    all_results.extend(run_coupling_sweep(3, max_level, n_samples))
    all_results.extend(run_activation_sweep(3, max_level, n_samples))
    all_results.extend(run_width_sweep(3, max_level, n_samples))
    all_results.extend(run_loss_sweep(3, max_level, n_samples))

    print_summary_table(all_results)
    return all_results


def print_summary_table(results):
    """Print a formatted comparison table."""
    print(f"\n{'=' * 90}")
    print("NEURAL NETWORK ALGEBRA SUMMARY")
    print(f"{'=' * 90}")
    print(f"  {'Config':<45s} {'Dims':<25s} {'Match 116?':<12s} Status")
    print(f"  {'-' * 88}")

    for r in results:
        L = r["n_layers"]
        k = r["width"]
        c = r["coupling_type"]
        loss = r["loss_function"]
        act = r["activation"]
        dims = r["dimension_sequence"]
        config = f"L={L} k={k} {c} {loss} {act}"

        dims_str = str(dims)
        if len(dims) >= 4 and dims[3] == 116:
            match = "YES"
        elif len(dims) >= 4:
            match = f"NO ({dims[3]})"
        else:
            match = "N/A"

        status = "STABLE" if r["stabilized"] else "GROWING"
        print(f"  {config:<45s} {dims_str:<25s} {match:<12s} {status}")

    # Check for universality classes
    seq_groups = {}
    for r in results:
        key = tuple(r["dimension_sequence"])
        if key not in seq_groups:
            seq_groups[key] = []
        config = (f"L={r['n_layers']} k={r['width']} "
                  f"{r['coupling_type']} {r['loss_function']} "
                  f"{r['activation']}")
        seq_groups[key].append(config)

    print(f"\n  UNIVERSALITY CLASSES:")
    for seq, configs in sorted(seq_groups.items()):
        print(f"  {list(seq)}: {len(configs)} configurations")
        for c in configs:
            print(f"    - {c}")


