def main():
    ap = argparse.ArgumentParser(
        description="Generalized Neural Network Poisson Algebra")
    ap.add_argument("--n-layers", type=int, default=3)
    ap.add_argument("--width", type=int, default=1)
    ap.add_argument("--coupling", type=str, default="gradient",
                    choices=["gradient", "hessian", "symmetric", "fisher",
                             "gradient_sum", "gradient_abs", "hessian_plain",
                             "hessian_full", "directional", "natural_gradient",
                             "loss_power", "gradient_cubic"])
    ap.add_argument("--loss", type=str, default="mse",
                    choices=["mse", "cross_entropy", "l1"])
    ap.add_argument("--activation", type=str, default="linear",
                    choices=["linear", "tanh_taylor", "relu_taylor"])
    ap.add_argument("--max-level", type=int, default=3)
    ap.add_argument("--samples", type=int, default=500)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--sweep", type=str, default=None,
                    choices=["depth", "coupling", "activation",
                             "width", "loss", "full"])
    args = ap.parse_args()

    if args.sweep:
        if args.sweep == "depth":
            run_depth_sweep(args.max_level, args.samples)
        elif args.sweep == "coupling":
            run_coupling_sweep(args.n_layers, args.max_level, args.samples)
        elif args.sweep == "activation":
            run_activation_sweep(args.n_layers, args.max_level, args.samples)
        elif args.sweep == "width":
            run_width_sweep(args.n_layers, args.max_level, args.samples)
        elif args.sweep == "loss":
            run_loss_sweep(args.n_layers, args.max_level, args.samples)
        elif args.sweep == "full":
            run_full_sweep(args.max_level, args.samples)
    else:
        r = compute_nn_algebra(
            n_layers=args.n_layers,
            width=args.width,
            coupling_type=args.coupling,
            loss_fn=args.loss,
            activation=args.activation,
            max_level=args.max_level,
            n_samples=args.samples,
            seed=args.seed,
        )
        save_result(r)


if __name__ == "__main__":
    main()
