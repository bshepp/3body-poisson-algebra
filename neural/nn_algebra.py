# =====================================================================
# Dynamic phase-space construction
# =====================================================================
def build_phase_space(n_layers, width=1):
    """
    Build symbolic phase-space variables for an L-layer, width-k network.

    Returns:
        weights: list of symbols [w_1, ..., w_L] (or w_i_j for width>1)
        momenta: list of symbols [v_1, ..., v_L]
        q_vars: flat list of all position symbols
        p_vars: flat list of all momentum symbols
    """
    weights = []
    momenta = []
    q_vars = []
    p_vars = []

    if width == 1:
        for i in range(1, n_layers + 1):
            w = Symbol(f"w{i}", real=True)
            v = Symbol(f"v{i}", real=True)
            weights.append(w)
            momenta.append(v)
            q_vars.append(w)
            p_vars.append(v)
    else:
        for i in range(1, n_layers + 1):
            layer_w = []
            layer_v = []
            for j in range(1, width + 1):
                w = Symbol(f"w{i}_{j}", real=True)
                v = Symbol(f"v{i}_{j}", real=True)
                layer_w.append(w)
                layer_v.append(v)
                q_vars.append(w)
                p_vars.append(v)
            weights.append(layer_w)
            momenta.append(layer_v)

    return weights, momenta, q_vars, p_vars


def poisson_bracket_generic(f, g, q_vars, p_vars):
    """
    Generic Poisson bracket for polynomial expressions.
    No chain rule needed since NN losses are polynomial in weights.

    {f, g} = sum_i (df/dq_i * dg/dp_i - df/dp_i * dg/dq_i)
    """
    result = Integer(0)
    for q, p in zip(q_vars, p_vars):
        result += diff(f, q) * diff(g, p) - diff(f, p) * diff(g, q)
    return result


def simplify_gen(expr):
    """Light simplification for polynomial expressions."""
    return cancel(expr)


# =====================================================================
# Loss function builders
# =====================================================================
def build_loss_mse(weights, width=1):
    """
    MSE loss for a linear network: L = (w_L * ... * w_1 * x - t)^2 / 2
    With x=1, t=1: L = (prod(w_i) - 1)^2 / 2
    """
    if width == 1:
        product = Integer(1)
        for w in weights:
            product *= w
        return (product - 1)**2 / 2, product
    else:
        product = weights[0][0]
        for layer in weights[1:]:
            product *= layer[0]
        return (product - 1)**2 / 2, product


def build_loss_l1(weights, width=1):
    """
    L1 loss approximated via smooth abs: L = |prod(w_i) - 1|
    We use a polynomial Taylor approximation: |z| ≈ sqrt(z^2 + eps)
    For exact polynomial algebra, use z^2 as proxy (same algebra structure).
    """
    if width == 1:
        product = Integer(1)
        for w in weights:
            product *= w
    else:
        product = weights[0][0]
        for layer in weights[1:]:
            product *= layer[0]
    z = product - 1
    return z**2, product


def build_loss_cross_entropy(weights, width=1):
    """
    Cross-entropy approximation for linear networks.
    CE = -log(sigma(f(x))) where sigma is sigmoid.
    Taylor expand: -log(sigmoid(z)) ≈ z^2/4 + z^4/48 + ... for small z.
    We use the leading quadratic + quartic terms.
    """
    if width == 1:
        product = Integer(1)
        for w in weights:
            product *= w
    else:
        product = weights[0][0]
        for layer in weights[1:]:
            product *= layer[0]
    z = product - 1
    return z**2 / 4 + z**4 / 48, product


# =====================================================================
# Coupling type builders
# =====================================================================
def build_couplings(weights, momenta, loss_func, width=1,
                    coupling_type="gradient", n_layers=None):
    """
    Build pairwise Hamiltonians H_ij for all pairs (i,j).

    Returns dict of {(i,j): H_ij} where i < j (0-indexed layer pairs).
    """
    if n_layers is None:
        n_layers = len(weights)

    L, product = loss_func(weights, width)

    if width == 1:
        gradients = [diff(L, w) for w in weights]
    else:
        gradients = [diff(L, w[0]) for w in weights]

    hamiltonians = {}

    for i, j in combinations(range(n_layers), 2):
        if width == 1:
            T_i = momenta[i]**2 / 2
            T_j = momenta[j]**2 / 2
        else:
            T_i = sum(v**2 for v in momenta[i]) / 2
            T_j = sum(v**2 for v in momenta[j]) / 2

        if coupling_type == "gradient":
            V_ij = gradients[i] * gradients[j] / 2

        elif coupling_type == "hessian":
            if width == 1:
                d2L = diff(gradients[i], weights[j])
                V_ij = d2L * weights[i] * weights[j] / 2
            else:
                d2L = diff(gradients[i], weights[j][0])
                V_ij = d2L * weights[i][0] * weights[j][0] / 2

        elif coupling_type == "symmetric":
            V_ij = L / sp.binomial(n_layers, 2)

        elif coupling_type == "fisher":
            V_ij = gradients[i] * gradients[j] * (1 - L)

        elif coupling_type == "gradient_sum":
            V_ij = (gradients[i]**2 + gradients[j]**2) / 2

        elif coupling_type == "gradient_abs":
            V_ij = (gradients[i] + gradients[j])**2 / 2

        elif coupling_type == "hessian_plain":
            if width == 1:
                d2L = diff(gradients[i], weights[j])
                V_ij = d2L / 2
            else:
                d2L = diff(gradients[i], weights[j][0])
                V_ij = d2L / 2

        elif coupling_type == "hessian_full":
            if width == 1:
                hii = diff(gradients[i], weights[i])
                hjj = diff(gradients[j], weights[j])
                hij = diff(gradients[i], weights[j])
                V_ij = (hii * weights[i]**2 + hjj * weights[j]**2
                        + 2 * hij * weights[i] * weights[j]) / 2
            else:
                hii = diff(gradients[i], weights[i][0])
                hjj = diff(gradients[j], weights[j][0])
                hij = diff(gradients[i], weights[j][0])
                V_ij = (hii * weights[i][0]**2 + hjj * weights[j][0]**2
                        + 2 * hij * weights[i][0] * weights[j][0]) / 2

        elif coupling_type == "directional":
            V_ij = gradients[i] * momenta[i] + gradients[j] * momenta[j] \
                if width == 1 else \
                gradients[i] * momenta[i][0] + gradients[j] * momenta[j][0]

        elif coupling_type == "natural_gradient":
            V_ij = gradients[i] * gradients[j] * (1 + L / 2)

        elif coupling_type == "loss_power":
            V_ij = L**2 / sp.binomial(n_layers, 2)

        elif coupling_type == "gradient_cubic":
            V_ij = gradients[i]**2 * gradients[j] + gradients[i] * gradients[j]**2

        else:
            raise ValueError(f"Unknown coupling type: {coupling_type}")

        hamiltonians[(i, j)] = expand(T_i + T_j + V_ij)

    return hamiltonians


# =====================================================================
# Activation support (Taylor expansion approach)
# =====================================================================
def build_loss_with_activation(weights, activation="linear",
                               taylor_order=3, width=1):
    """
    Build loss with nonlinear activation applied between layers.

    For activation='linear': standard product
    For activation='tanh_taylor': tanh(z) ≈ z - z^3/3 (order-3 Taylor)
    For activation='relu_taylor': ReLU ≈ z/2 + z^2/4 (smooth approx)

    Uses minimal Taylor order to keep polynomial degree manageable.
    """
    if activation == "linear":
        return build_loss_mse(weights, width)

    if width == 1:
        z = weights[0]
        for w in weights[1:]:
            if activation == "tanh_taylor":
                z = z - z**3 / 3
            elif activation == "relu_taylor":
                z = z / 2 + z**2 / 4
            z = w * z
    else:
        z = weights[0][0]
        for w in weights[1:]:
            if activation == "tanh_taylor":
                z = z - z**3 / 3
            elif activation == "relu_taylor":
                z = z / 2 + z**2 / 4
            z = w[0] * z

    loss = expand((z - 1)**2 / 2)
    return loss, z


# =====================================================================
# SVD gap analysis (self-contained)
# =====================================================================
def svd_gap_analysis(eval_matrix, label=""):
    """Compute SVD, find gaps, return rank."""
    norms = np.linalg.norm(eval_matrix, axis=0)
    norms[norms < 1e-15] = 1.0
    M = eval_matrix / norms

    U, s, Vt = np.linalg.svd(M, full_matrices=False)

    noise_threshold = 1e-8 * s[0]
    n_meaningful = int(np.sum(s > noise_threshold))

    best_gap_ratio = 1.0
    best_gap_idx = -1
    for i in range(min(n_meaningful, len(s) - 1)):
        if s[i + 1] > noise_threshold:
            gap = s[i] / s[i + 1]
        else:
            gap = s[i] / max(s[i + 1], 1e-300)
        if gap > best_gap_ratio and i >= 1:
            best_gap_ratio = gap
            best_gap_idx = i

    if best_gap_ratio > 1e4:
        rank = best_gap_idx + 1
    elif best_gap_ratio > 10:
        rank = best_gap_idx + 1
    else:
        rank = n_meaningful

    return rank, s


