# Phase-space sampling
# =====================================================================
def sample_phase_space_nn(n_dims, n_samples, seed=42, w_range=3.0, v_range=1.0):
    """
    Sample phase-space points for a system with n_dims total
    (n_dims/2 positions + n_dims/2 momenta).
    """
    rng = np.random.RandomState(seed)
    n_q = n_dims // 2
    Z = np.zeros((n_samples, n_dims))
    Z[:, :n_q] = rng.uniform(-w_range, w_range, (n_samples, n_q))
    Z[:, n_q:] = rng.uniform(-v_range, v_range, (n_samples, n_q))
    return Z


def _make_flat_func(expr, all_vars, label="_f"):
    """Build a flat function via temp file to bypass compiler recursion."""
    import tempfile
    import importlib.util
    from sympy import pycode

    var_names = [str(v) for v in all_vars]
    sig = ", ".join(var_names)

    terms = sp.Add.make_args(expr)
    lines = [
        "import numpy as _np",
        "from numpy import exp, log, sqrt, sin, cos, abs, power",
        f"def {label}({sig}):",
    ]
    max_chunk = 50
    if len(terms) <= max_chunk:
        lines.append(f"    _result = {pycode(expr)}")
    else:
        lines.append("    _result = 0")
        for i in range(0, len(terms), max_chunk):
            chunk = sp.Add(*terms[i:i + max_chunk])
            lines.append(f"    _result += {pycode(chunk)}")
    lines.append("    return _result")

    code = "\n".join(lines)
    tmp = tempfile.NamedTemporaryFile(
        mode="w", suffix=".py", delete=False, prefix="flat_")
    tmp.write(code)
    tmp.flush()
    tmp.close()

    spec = importlib.util.spec_from_file_location(f"_flat_{label}", tmp.name)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return getattr(mod, label)


def lambdify_exprs(exprs, all_vars):
    """Convert SymPy expressions to fast numpy callables."""
    n = len(exprs)
    funcs = []
    for idx, expr in enumerate(exprs):
        try:
            f = sp.lambdify(all_vars, expr, modules="numpy", cse=False)
            funcs.append(f)
        except (RecursionError, Exception):
            try:
                f = _make_flat_func(expr, all_vars, label=f"g{idx}")
                funcs.append(f)
            except (RecursionError, Exception):
                f = sp.lambdify(all_vars, expr, modules="numpy", cse=True)
                funcs.append(f)

    def evaluate(Z):
        args = [Z[:, i] for i in range(Z.shape[1])]
        n_pts = Z.shape[0]
        cols = []
        for f in funcs:
            val = f(*args)
            arr = np.atleast_1d(np.asarray(val, dtype=float)).ravel()
            if arr.shape[0] == 1:
                arr = np.full(n_pts, arr[0])
            cols.append(arr[:n_pts])
        return np.column_stack(cols)

    return evaluate


# =====================================================================
# Core computation
# =====================================================================
def compute_nn_algebra(n_layers=3, width=1, coupling_type="gradient",
                       loss_fn="mse", activation="linear",
                       max_level=3, n_samples=500, seed=42,
                       verbose=True):
    """
    Compute the Poisson bracket Lie algebra for a neural network.

    Returns a result dict with dimension sequence and metadata.
    """
    t_start = time()

    weights, momenta, q_vars, p_vars = build_phase_space(n_layers, width)
    all_vars = q_vars + p_vars
    n_dims = len(all_vars)
    n_pairs = n_layers * (n_layers - 1) // 2

    if verbose:
        print("=" * 70)
        print(f"NEURAL NETWORK POISSON ALGEBRA")
        print(f"  Layers: {n_layers}, Width: {width}, "
              f"Phase space: {n_dims}D")
        print(f"  Coupling: {coupling_type}, Loss: {loss_fn}, "
              f"Activation: {activation}")
        print(f"  Max level: {max_level}, Samples: {n_samples}")
        print("=" * 70)

    if activation != "linear":
        loss_builder = lambda w, k: build_loss_with_activation(
            w, activation=activation, width=k)
    elif loss_fn == "mse":
        loss_builder = build_loss_mse
    elif loss_fn == "l1":
        loss_builder = build_loss_l1
    elif loss_fn == "cross_entropy":
        loss_builder = build_loss_cross_entropy
    else:
        raise ValueError(f"Unknown loss function: {loss_fn}")

    hamiltonians = build_couplings(
        weights, momenta, loss_builder,
        width=width, coupling_type=coupling_type, n_layers=n_layers
    )

    pair_keys = sorted(hamiltonians.keys())
    all_exprs = []
    all_names = []
    all_levels = []
    computed_pairs = set()

    if verbose:
        print(f"\n--- Level 0: {n_pairs} Pairwise Hamiltonians ---")
    for idx, (i, j) in enumerate(pair_keys):
        expr = hamiltonians[(i, j)]
        name = f"H{i+1}{j+1}"
        all_exprs.append(expr)
        all_names.append(name)
        all_levels.append(0)
        nterms = len(Add.make_args(expr))
        if verbose:
            print(f"  {name}: {nterms} terms")

    for i in range(n_pairs):
        for j in range(i + 1, n_pairs):
            computed_pairs.add(frozenset({i, j}))

    # Level 1
    if max_level >= 1:
        if verbose:
            print(f"\n--- Level 1 ---")
        t_lv = time()
        new_exprs = []
        new_names = []
        n_cand = 0
        for i in range(n_pairs):
            for j in range(i + 1, n_pairs):
                n_cand += 1
                t0 = time()
                expr = poisson_bracket_generic(
                    all_exprs[i], all_exprs[j], q_vars, p_vars)
                expr = simplify_gen(expr)
                nterms = len(Add.make_args(expr))
                if verbose:
                    print(f"  [{n_cand}] {{{all_names[i]},{all_names[j]}}} "
                          f"-> {nterms} terms [{time()-t0:.1f}s]")
                new_exprs.append(expr)
                bname = f"{{{all_names[i]},{all_names[j]}}}"
                new_names.append(bname)

        for expr, name in zip(new_exprs, new_names):
            all_exprs.append(expr)
            all_names.append(name)
            all_levels.append(1)

        if verbose:
            n_zero = sum(1 for e in new_exprs if e == 0)
            print(f"  Level 1: {len(new_exprs)} candidates "
                  f"({n_zero} zero) [{time()-t_lv:.1f}s]")

    # Levels 2+
    for level in range(2, max_level + 1):
        if verbose:
            print(f"\n--- Level {level} ---")
        t_lv = time()

        frontier = [i for i, lv in enumerate(all_levels) if lv == level - 1]
        n_existing = len(all_exprs)
        new_exprs = []
        new_names = []
        n_cand = 0

        for i in frontier:
            for j in range(n_existing):
                if i == j:
                    continue
                pair = frozenset({i, j})
                if pair in computed_pairs:
                    continue
                computed_pairs.add(pair)
                if all_exprs[i] == 0 or all_exprs[j] == 0:
                    continue

                n_cand += 1
                t0 = time()
                expr = poisson_bracket_generic(
                    all_exprs[i], all_exprs[j], q_vars, p_vars)
                t_b = time() - t0
                t0s = time()
                expr = simplify_gen(expr)
                t_s = time() - t0s
                nterms = len(Add.make_args(expr))

                if verbose and (n_cand <= 20 or n_cand % 50 == 0):
                    print(f"  [{n_cand:>4d}] {{{all_names[i]},{all_names[j]}}} "
                          f"bracket {t_b:.1f}s simplify {t_s:.1f}s "
                          f"-> {nterms} terms")

                new_exprs.append(expr)
                new_names.append(f"{{{all_names[i]},{all_names[j]}}}")

        for expr, name in zip(new_exprs, new_names):
            all_exprs.append(expr)
            all_names.append(name)
            all_levels.append(level)

        if verbose:
            n_zero = sum(1 for e in new_exprs if e == 0)
            print(f"  Level {level}: {len(new_exprs)} candidates "
                  f"({n_zero} zero) [{time()-t_lv:.1f}s]")

    # SVD analysis
    nonzero_mask = [i for i, e in enumerate(all_exprs) if e != 0]
    nonzero_exprs = [all_exprs[i] for i in nonzero_mask]
    nonzero_levels = [all_levels[i] for i in nonzero_mask]

    if verbose:
        print(f"\n--- SVD Analysis ---")
        print(f"  Total generators: {len(all_exprs)} "
              f"(non-zero: {len(nonzero_exprs)})")

    if len(nonzero_exprs) == 0:
        dims = [n_pairs] + [n_pairs] * max_level
        return _build_result(dims, n_layers, width, coupling_type,
                             loss_fn, activation, max_level,
                             len(all_exprs), time() - t_start)

    Z = sample_phase_space_nn(n_dims, n_samples, seed)
    evaluate = lambdify_exprs(nonzero_exprs, all_vars)
    eval_matrix = evaluate(Z)

    if verbose:
        print(f"  Eval matrix: {eval_matrix.shape}")

    level_dims = {}
    for lv in range(max_level + 1):
        mask = [i for i, l in enumerate(nonzero_levels) if l <= lv]
        if not mask:
            level_dims[lv] = 0
            continue
        sub = eval_matrix[:, mask]
        rank, _ = svd_gap_analysis(sub, label=f"level {lv}")
        level_dims[lv] = rank
        if verbose:
            print(f"  Dim through level {lv}: {rank}")

    dims = [level_dims[lv] for lv in range(max_level + 1)]
    new_per_level = [dims[0]]
    for i in range(1, len(dims)):
        new_per_level.append(dims[i] - dims[i - 1])

    elapsed = time() - t_start

    if verbose:
        print(f"\n  Dimension sequence: {dims}")
        print(f"  New per level: {new_per_level}")
        print(f"  Total time: {elapsed:.1f}s")

    return _build_result(dims, n_layers, width, coupling_type,
                         loss_fn, activation, max_level,
                         len(all_exprs), elapsed)


def _build_result(dims, n_layers, width, coupling_type,
                  loss_fn, activation, max_level, n_generators, elapsed):
    new_per_level = [dims[0]]
    for i in range(1, len(dims)):
        new_per_level.append(dims[i] - dims[i - 1])

    stabilized = False
    for i in range(1, len(dims)):
        if dims[i] == dims[i - 1]:
            stabilized = True
            break

    return {
        "n_layers": n_layers,
        "width": width,
        "coupling_type": coupling_type,
        "loss_function": loss_fn,
        "activation": activation,
        "max_level": max_level,
        "dimension_sequence": dims,
        "new_per_level": new_per_level,
        "n_generators": n_generators,
        "stabilized": stabilized,
        "computation_time_seconds": round(elapsed, 1),
        "computation_method": "numerical_SVD",
    }


def save_result(result, output_dir="results/neural_algebras"):
    """Save result as JSON."""
    os.makedirs(output_dir, exist_ok=True)
    L = result["n_layers"]
    k = result["width"]
    c = result["coupling_type"]
    loss = result["loss_function"]
    act = result["activation"]
    fname = f"nn_L{L}_k{k}_{c}_{loss}_{act}.json"
    path = os.path.join(output_dir, fname)
    with open(path, "w") as f:
        json.dump(result, f, indent=2)
    print(f"  Saved: {path}")
    return path


