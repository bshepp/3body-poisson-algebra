    results = []
    for k in [1, 2, 3]:
        print(f"\n{'#' * 70}")
        print(f"# WIDTH SWEEP: k={k}")
        print(f"{'#' * 70}")
        r = compute_nn_algebra(
            n_layers=n_layers, width=k,
            coupling_type="gradient",
            max_level=max_level, n_samples=n_samples)
        save_result(r)
