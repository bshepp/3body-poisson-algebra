#!/usr/bin/env python3
"""
Generalized Neural Network Poisson Algebra Engine
===================================================

Computes the Lie algebra generated by pairwise Hamiltonians of
neural network training dynamics for arbitrary:

  - n_layers (L): number of scalar weight layers (L >= 2)
  - width (k): weight dimension per layer (k=1 scalar, k>1 vector)
  - coupling_type: gradient-product, hessian, symmetric, fisher
  - loss_function: mse, cross_entropy, l1
  - activation: linear, relu_taylor, tanh_taylor

Phase space is 2*L*k dimensional for width-k networks.
For k=1, each weight w_i is a scalar with conjugate momentum v_i.

Usage
-----
    python nn_algebra.py --n-layers 3 --coupling gradient
    python nn_algebra.py --n-layers 4 --coupling gradient --max-level 2
    python nn_algebra.py --sweep depth     # L=2..5, gradient coupling
    python nn_algebra.py --sweep coupling  # all couplings, L=3
    python nn_algebra.py --sweep full      # all combinations
"""

import os
import sys
import json
import argparse
import numpy as np
from time import time
from itertools import combinations

import sympy as sp
