# =============================================================================
# Phase-space sampling
# =============================================================================
def sample_phase_space(n, seed=42, pos_range=3.0, mom_range=1.0, min_sep=0.5):
    rng = np.random.RandomState(seed)
    points = np.empty((0, NDIM))
    for _ in range(200):
        batch_size = max((n - points.shape[0]) * 5, 256)
        batch = np.zeros((batch_size, NDIM))
        batch[:, :6] = rng.uniform(-pos_range, pos_range, (batch_size, 6))
        batch[:, 6:] = rng.uniform(-mom_range, mom_range, (batch_size, 6))
        dx12 = batch[:, 0] - batch[:, 2]
        dy12 = batch[:, 1] - batch[:, 3]
        dx13 = batch[:, 0] - batch[:, 4]
        dy13 = batch[:, 1] - batch[:, 5]
        dx23 = batch[:, 2] - batch[:, 4]
        dy23 = batch[:, 3] - batch[:, 5]
        r12 = np.sqrt(dx12**2 + dy12**2)
        r13 = np.sqrt(dx13**2 + dy13**2)
        r23 = np.sqrt(dx23**2 + dy23**2)
        good = (r12 > min_sep) & (r13 > min_sep) & (r23 > min_sep)
        points = np.vstack([points, batch[good]])
        if points.shape[0] >= n:
            break
    return points[:n]


# =============================================================================
# Gravitational 3-body Hamiltonians
# =============================================================================
class GravitationalAlgebra:
    """Planar gravitational 3-body: V_ij = -G*m_i*m_j / r_ij"""

    def __init__(self, m1=1.0, m2=1.0, m3=1.0, G=1.0):
        self.m1, self.m2, self.m3, self.G = m1, m2, m3, G
        self.name = "Gravitational"

    def H12(self, Z):
        px1, py1 = Z[:, 6], Z[:, 7]
        px2, py2 = Z[:, 8], Z[:, 9]
        T = (px1**2 + py1**2) / (2 * self.m1) + (px2**2 + py2**2) / (2 * self.m2)
        dx = Z[:, 0] - Z[:, 2]
        dy = Z[:, 1] - Z[:, 3]
        r = np.sqrt(dx**2 + dy**2)
        return T - self.G * self.m1 * self.m2 / r

    def H13(self, Z):
        px1, py1 = Z[:, 6], Z[:, 7]
        px3, py3 = Z[:, 10], Z[:, 11]
        T = (px1**2 + py1**2) / (2 * self.m1) + (px3**2 + py3**2) / (2 * self.m3)
        dx = Z[:, 0] - Z[:, 4]
        dy = Z[:, 1] - Z[:, 5]
        r = np.sqrt(dx**2 + dy**2)
        return T - self.G * self.m1 * self.m3 / r

    def H23(self, Z):
        px2, py2 = Z[:, 8], Z[:, 9]
        px3, py3 = Z[:, 10], Z[:, 11]
        T = (px2**2 + py2**2) / (2 * self.m2) + (px3**2 + py3**2) / (2 * self.m3)
        dx = Z[:, 2] - Z[:, 4]
        dy = Z[:, 3] - Z[:, 5]
        r = np.sqrt(dx**2 + dy**2)
        return T - self.G * self.m2 * self.m3 / r

    def K1(self, Z):
        """{H12, H13}"""
        px1, py1 = Z[:, 6], Z[:, 7]
        dx12 = Z[:, 0] - Z[:, 2]; dy12 = Z[:, 1] - Z[:, 3]
        dx13 = Z[:, 0] - Z[:, 4]; dy13 = Z[:, 1] - Z[:, 5]
        r12 = np.sqrt(dx12**2 + dy12**2)
        r13 = np.sqrt(dx13**2 + dy13**2)
        t13 = self.G * self.m3 * ((-dx13) * px1 + (-dy13) * py1) / r13**3
        t12 = self.G * self.m2 * ((-dx12) * px1 + (-dy12) * py1) / r12**3
        return t13 - t12

    def K2(self, Z):
        """{H12, H23}"""
        px2, py2 = Z[:, 8], Z[:, 9]
        dx12 = Z[:, 0] - Z[:, 2]; dy12 = Z[:, 1] - Z[:, 3]
        dx23 = Z[:, 2] - Z[:, 4]; dy23 = Z[:, 3] - Z[:, 5]
        r12 = np.sqrt(dx12**2 + dy12**2)
        r23 = np.sqrt(dx23**2 + dy23**2)
        t12 = self.G * self.m1 * ((-dx12) * px2 + (-dy12) * py2) / r12**3
        t23 = self.G * self.m3 * (dx23 * px2 + dy23 * py2) / r23**3
        return t12 - t23

    def K3(self, Z):
        """{H13, H23}"""
        px3, py3 = Z[:, 10], Z[:, 11]
        dx13 = Z[:, 0] - Z[:, 4]; dy13 = Z[:, 1] - Z[:, 5]
        dx23 = Z[:, 2] - Z[:, 4]; dy23 = Z[:, 3] - Z[:, 5]
        r13 = np.sqrt(dx13**2 + dy13**2)
        r23 = np.sqrt(dx23**2 + dy23**2)
        t13 = self.G * self.m1 * ((-dx13) * px3 + (-dy13) * py3) / r13**3
        t23 = self.G * self.m2 * ((-dx23) * px3 + (-dy23) * py3) / r23**3
        return t13 - t23


